package ir.madadyar.util

object ApiErrorMapper {

	fun messageFor(code: Int, serverMessage: String?): String {
		return when (code) {
			200 -> "درخواست با موفقیت انجام شد"
			401 -> "دسترسی غیرمجاز"
			404 -> "صفحه یافت نشد"
			405 -> "مدل یافت نشد"
			406 -> "متد یافت نشد"
			407 -> "مسیر یافت نشد"
			422 -> "خطا در اعتبارسنجی اطلاعات"
			500 -> "خطای داخلی سرور"
			501 -> "خطای پایگاه داده"
			701 -> "خطای احراز هویت"
			702 -> "کد تأیید نامعتبر است"
			703 -> "حساب کاربری غیر فعال است"
			704 -> "کاربر یافت نشد یا توکن نامعتبر است"
			706 -> "تلاش‌های ورود بیش از حد مجاز"
			707 -> "نام کاربری یا رمز عبور نادرست است"
			708 -> "حجم درخواست بیش از حد مجاز است"
			709 -> "عملیات مهمان مجاز نیست"
			960 -> "حساب عامل تأیید نشده است"
			961 -> "ارسال پیامک با خطا مواجه شد"
			962 -> "درخواست بیش از حد کد یک‌بار مصرف"
			980 -> "احراز هویت مینی‌اپ نامعتبر است"
			982 -> "اعتبارسنجی موبایل مینی‌اپ ناموفق بود"
			990 -> "بروزرسانی پروفایل انجام نشد"
			else -> serverMessage ?: "خطایی رخ داده است"
		}
	}
}


